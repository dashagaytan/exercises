//for loop that prints out numbers 1-9
for(let i = 0; i < 10; i++){
    console.log(i);
}

// for loop that decrements 9-0
for(let i = 10; i >= 0; i--){
    console.log(i);
}

// for loop that prints an array of fruits
const fruit = ["banana", "orange", "apple", "kiwi"]

for (let f = 0; f < fruit.length; f++){
    console.log(fruit[f]);
}